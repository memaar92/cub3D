#include "../includes/cub.h"

void	set_char(char **buf, char c)
{
	int	i;

	i = -1;
	while ((*buf)[++i])
	{
		if (ft_isspace((*buf)[i]) || (*buf)[i] == c)
			(*buf)[i] = ' ';
	}
}
